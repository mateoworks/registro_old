<?php 

	class LoginModel extends Mysql
	{
		private $intIdUsuario;
		private $strUsuario;
		private $strPassword;
		private $strToken;

		public function __construct()
		{
			parent::__construct();
		}	

		public function loginUser(string $usuario, string $password)
		{
			$this->strUsuario = $usuario;
			$this->strPassword = $password;
			$sql = "SELECT id, estado FROM usuarios WHERE 
					email = '$this->strUsuario' and 
					password = '$this->strPassword' and 
					estado != 0 ";
			$request = $this->select($sql);
			return $request;
		}

		public function sessionLogin(int $iduser){
			$this->intIdUsuario = $iduser;
			//BUSCAR ROLE 
			$sql = "SELECT u.id as idpersona,
							u.nombre_propio as nombres,
							u.apellidos,
							u.telefono,
							u.email,
							r.id as idrol,
							r.nombre_rol as nombrerol,
							u.estado,
							IFNULL(s.id, 0) as seccional_id,
							IFNULL(s.nombre_seccional, 'Supremo líder estatal') as nombre_seccional
					FROM usuarios u
					INNER JOIN roles r
					ON u.rol_id = r.id
					LEFT JOIN seccionales s
					ON r.seccional = s.id
					WHERE u.id = $this->intIdUsuario";
			$request = $this->select($sql);
			$_SESSION['userData'] = $request;
			return $request;
		}

		public function getUserEmail(string $strEmail){
			$this->strUsuario = $strEmail;
			$sql = "SELECT id, nombre_propio, apellidos, estado FROM usuarios WHERE 
					email = '$this->strUsuario' and  
					estado = 1 ";
			$request = $this->select($sql);
			return $request;
		}

		public function setTokenUser(int $idpersona, string $token){
			$this->intIdUsuario = $idpersona;
			$this->strToken = $token;
			$sql = "UPDATE usuarios SET token = ? WHERE id = $this->intIdUsuario ";
			$arrData = array($this->strToken);
			$request = $this->update($sql,$arrData);
			return $request;
		}

		public function getUsuario(string $email, string $token){
			$this->strUsuario = $email;
			$this->strToken = $token;
			$sql = "SELECT id FROM usuarios WHERE 
					email = '$this->strUsuario' and 
					token = '$this->strToken' and 					
					estado = 1 ";
			$request = $this->select($sql);
			return $request;
		}

		public function insertPassword(int $idPersona, string $password){
			$this->intIdUsuario = $idPersona;
			$this->strPassword = $password;
			$sql = "UPDATE usuarios SET password = ?, token = ? WHERE id = $this->intIdUsuario ";
			$arrData = array($this->strPassword,"");
			$request = $this->update($sql,$arrData);
			return $request;
		}
	}
 ?>